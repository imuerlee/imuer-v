import '../../domain/entities/server_node.dart';

class ServerNodeModel extends ServerNode {
  const ServerNodeModel({
    required super.id,
    required super.name,
    required super.address,
    required super.port,
    required super.protocol,
    super.username,
    super.password,
    super.uuid,
    super.alterId,
    super.security,
    super.network,
    super.tls,
    super.host,
    super.path,
    super.sni,
    super.alpn,
    super.country,
    super.city,
    super.latitude,
    super.longitude,
    super.latency,
    super.downloadSpeed,
    super.isActive,
    super.groupId,
    required super.createdAt,
    required super.updatedAt,
    super.flow,
    super.method,
    super.plugin,
    super.pluginOpts,
    super.protocolParam,
    super.obfs,
    super.obfsParam,
    super.publicKey,
    super.privateKey,
    super.peerPublicKey,
    super.presharedKey,
    super.mtu,
    super.reserved,
    super.obfsPassword,
    super.speedLimit,
    super.speedLimitUp,
    super.speedLimitDown,
    super.auth,
    super.authUsername,
    super.authPassword,
    super.allowInsecure,
    super.fingerprint,
    super.verifyHostname,
    super.publicKey1,
    super.shortId,
  });

  factory ServerNodeModel.fromJson(Map<String, dynamic> json) {
    return ServerNodeModel(
      id: json['id'] as String,
      name: json['name'] as String,
      address: json['address'] as String,
      port: json['port'] as int,
      protocol: ServerProtocol.values.firstWhere(
        (e) => e.name == json['protocol'],
        orElse: () => ServerProtocol.vmess,
      ),
      username: json['username'] as String?,
      password: json['password'] as String?,
      uuid: json['uuid'] as String?,
      alterId: json['alter_id'] as int?,
      security: json['security'] as String?,
      network: json['network'] as String?,
      tls: json['tls'] as String?,
      host: json['host'] as String?,
      path: json['path'] as String?,
      sni: json['sni'] as String?,
      alpn: json['alpn'] as String?,
      fingerprint: json['fingerprint'] as String?,
      verifyHostname: json['verify_hostname'] == 1,
      publicKey: json['public_key'] as String?,
      privateKey: json['private_key'] as String?,
      peerPublicKey: json['peer_public_key'] as String?,
      presharedKey: json['preshared_key'] as String?,
      mtu: json['mtu'] as int?,
      reserved: json['reserved'] as String?,
      obfsPassword: json['obfs_password'] as String?,
      speedLimit: json['speed_limit'] as int?,
      speedLimitUp: json['speed_limit_up'] as int?,
      speedLimitDown: json['speed_limit_down'] as int?,
      auth: json['auth'] == 1,
      authUsername: json['auth_username'] as String?,
      authPassword: json['auth_password'] as String?,
      allowInsecure: json['allow_insecure'] == 1,
      flow: json['flow'] as String?,
      method: json['method'] as String?,
      plugin: json['plugin'] as String?,
      pluginOpts: json['plugin_opts'] as String?,
      protocolParam: json['protocol_param'] as String?,
      obfs: json['obfs'] as String?,
      obfsParam: json['obfs_param'] as String?,
      publicKey1: json['public_key1'] as String?,
      shortId: json['short_id'] as String?,
      country: json['country'] as String?,
      city: json['city'] as String?,
      latitude: (json['latitude'] as num?)?.toDouble(),
      longitude: (json['longitude'] as num?)?.toDouble(),
      latency: json['latency'] as int?,
      downloadSpeed: json['download_speed'] as int?,
      isActive: json['is_active'] == 1,
      groupId: json['group_id'] as String?,
      createdAt: DateTime.tryParse(json['created_at'] as String) ?? DateTime.now(),
      updatedAt: DateTime.tryParse(json['updated_at'] as String) ?? DateTime.now(),
      subscriptionUrl: json['subscription_url'] as String?,
      subscriptionDownload: json['subscription_download'] as int?,
      subscriptionUpload: json['subscription_upload'] as int?,
      subscriptionTotal: json['subscription_total'] as int?,
      subscriptionExpire: json['subscription_expire'] != null 
          ? DateTime.tryParse(json['subscription_expire'] as String)
          : null,
    );
  }

  @override
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'address': address,
      'port': port,
      'protocol': protocol.name,
      'username': username,
      'password': password,
      'uuid': uuid,
      'alter_id': alterId,
      'security': security,
      'network': network,
      'tls': tls,
      'host': host,
      'path': path,
      'sni': sni,
      'alpn': alpn,
      'fingerprint': fingerprint,
      'verify_hostname': verifyHostname == true ? 1 : 0,
      'public_key': publicKey,
      'private_key': privateKey,
      'peer_public_key': peerPublicKey,
      'preshared_key': presharedKey,
      'mtu': mtu,
      'reserved': reserved,
      'obfs_password': obfsPassword,
      'speed_limit': speedLimit,
      'speed_limit_up': speedLimitUp,
      'speed_limit_down': speedLimitDown,
      'auth': auth == true ? 1 : 0,
      'auth_username': authUsername,
      'auth_password': authPassword,
      'allow_insecure': allowInsecure == true ? 1 : 0,
      'flow': flow,
      'method': method,
      'plugin': plugin,
      'plugin_opts': pluginOpts,
      'protocol_param': protocolParam,
      'obfs': obfs,
      'obfs_param': obfsParam,
      'public_key1': publicKey1,
      'short_id': shortId,
      'country': country,
      'city': city,
      'latitude': latitude,
      'longitude': longitude,
      'latency': latency,
      'download_speed': downloadSpeed,
      'is_active': isActive == true ? 1 : 0,
      'group_id': groupId,
      'created_at': createdAt.toIso8601String(),
      'updated_at': updatedAt.toIso8601String(),
      'subscription_url': subscriptionUrl,
      'subscription_download': subscriptionDownload,
      'subscription_upload': subscriptionUpload,
      'subscription_total': subscriptionTotal,
      'subscription_expire': subscriptionExpire?.toIso8601String(),
    };
  }

  factory ServerNodeModel.fromEntity(ServerNode entity) {
    return ServerNodeModel(
      id: entity.id,
      name: entity.name,
      address: entity.address,
      port: entity.port,
      protocol: entity.protocol,
      username: entity.username,
      password: entity.password,
      uuid: entity.uuid,
      alterId: entity.alterId,
      security: entity.security,
      network: entity.network,
      tls: entity.tls,
      host: entity.host,
      path: entity.path,
      sni: entity.sni,
      alpn: entity.alpn,
      country: entity.country,
      city: entity.city,
      latitude: entity.latitude,
      longitude: entity.longitude,
      latency: entity.latency,
      downloadSpeed: entity.downloadSpeed,
      isActive: entity.isActive,
      groupId: entity.groupId,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
      flow: entity.flow,
      method: entity.method,
      plugin: entity.plugin,
      pluginOpts: entity.pluginOpts,
      protocolParam: entity.protocolParam,
      obfs: entity.obfs,
      obfsParam: entity.obfsParam,
      publicKey: entity.publicKey,
      privateKey: entity.privateKey,
      peerPublicKey: entity.peerPublicKey,
      presharedKey: entity.presharedKey,
      mtu: entity.mtu,
      reserved: entity.reserved,
      obfsPassword: entity.obfsPassword,
      speedLimit: entity.speedLimit,
      speedLimitUp: entity.speedLimitUp,
      speedLimitDown: entity.speedLimitDown,
      auth: entity.auth,
      authUsername: entity.authUsername,
      authPassword: entity.authPassword,
      allowInsecure: entity.allowInsecure,
      fingerprint: entity.fingerprint,
      verifyHostname: entity.verifyHostname,
      publicKey1: entity.publicKey1,
      shortId: entity.shortId,
    );
  }
}
